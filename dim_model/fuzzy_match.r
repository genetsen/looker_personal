### function to match dirty to clean ####
closest_map_base <- function(dirty, clean, ignore_case = TRUE, max_dist = NULL) {
  stopifnot(length(clean) > 0)
  
  norm <- function(x) {
    x <- as.character(x)
    x <- trimws(x)
    if (ignore_case) x <- tolower(x)
    x2 <- suppressWarnings(iconv(x, to = "ASCII//TRANSLIT"))
    x[!is.na(x2)] <- x2[!is.na(x2)]
    gsub("\\s+", " ", x)
  }
  
  dirty_raw <- as.character(dirty)
  clean_raw <- as.character(clean)
  
  d <- norm(dirty_raw)
  c <- norm(clean_raw)
  
  # Distance matrix (rows: dirty, cols: clean)
  D <- adist(d, c)
  idx <- max.col(-D, ties.method = "first")
  dist <- D[cbind(seq_along(d), idx)]
  
  # Optional thresholding
  if (is.null(max_dist)) {
    dyn <- pmax(1, round(0.3 * pmax(nchar(d), 1)))  # ~30% of length
    ok <- dist <= dyn
  } else {
    ok <- dist <= max_dist
  }
  
  data.frame(
    dirty         = dirty_raw,                 # ✅ original values
    match         = ifelse(ok, clean_raw[idx], NA_character_),
    edit_distance = as.integer(dist),
    stringsAsFactors = FALSE
  )
}
closest_map_base(dirty, clean, ignore_case = TRUE, max_dist = NULL)

closest_map_fast <- function(
    dirty, clean,
    ignore_case = TRUE,
    method = "jw",          # "jw" distances are in [0,1]
    max_dist = NULL,        # set e.g. 0.12 for jw; or an integer for edit-distance methods
    jw_threshold = 0.15     # used only if method %in% c("jw","jaro") and max_dist is NULL
) {
  if (!requireNamespace("stringdist", quietly = TRUE)) {
    stop("Please install.packages('stringdist') to use this function.")
  }
  
  norm <- function(x) {
    x <- as.character(x)
    x <- trimws(x)
    if (ignore_case) x <- tolower(x)
    x2 <- suppressWarnings(iconv(x, to = "ASCII//TRANSLIT"))
    x[!is.na(x2)] <- x2[!is.na(x2)]
    gsub("\\s+", " ", x)
  }
  
  dirty_raw <- as.character(dirty)
  clean_raw <- as.character(clean)
  d <- norm(dirty_raw)
  c <- norm(clean_raw)
  
  keep_c <- !is.na(c)
  c2 <- c[keep_c]
  clean_kept <- clean_raw[keep_c]
  if (length(c2) == 0) {
    return(data.frame(dirty = dirty_raw, match = NA_character_, distance = NA_real_))
  }
  
  # Build safe per-element thresholds (never <= 0)
  if (!is.null(max_dist)) {
    mx <- rep(max_dist, length(d))
  } else if (method %in% c("jw", "jaro")) {
    mx <- rep(jw_threshold, length(d))  # jw scale: 0 identical … 1 very different
  } else {
    n <- nchar(d); n[is.na(n)] <- 0
    mx <- pmax(1, round(0.3 * pmax(n, 1)))  # edit distance allowance
  }
  
  idx <- vapply(seq_along(d), function(i) {
    di <- d[i]; mxi <- mx[i]
    if (is.na(di) || is.na(mxi) || !is.finite(mxi) || mxi <= 0) return(NA_integer_)
    stringdist::amatch(di, c2, method = method, maxDist = mxi)
  }, integer(1))
  
  dist <- ifelse(
    is.na(idx), NA_real_,
    stringdist::stringdist(d, c2[idx], method = method)
  )
  
  data.frame(
    dirty    = dirty_raw,                     # ✅ original values
    match    = ifelse(is.na(idx), NA, clean_kept[idx]),
    distance = dist,
    stringsAsFactors = FALSE
  )
}

closest_map_fast(dirty, clean)
closest_map_fast(dirty, clean, method = "jw")
 